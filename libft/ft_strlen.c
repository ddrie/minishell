int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	if (str[i] == '\0')
	{
		return (0);
	}
	else
	{
		i++;
		while (str[i] != '\0')
		{
			i = i + 1;
		}
	}
	return (i);
}
